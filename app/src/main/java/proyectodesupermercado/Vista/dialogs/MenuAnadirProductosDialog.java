    private void doSearch() {
        if (busquedaTextfield.getText().isBlank()) {
            refreshTable(buscadorDeInventario.refreshInitialModel());
        } else {
            refreshTable(buscadorDeInventario.search(busquedaTextfield.getText()));
        }
    }
